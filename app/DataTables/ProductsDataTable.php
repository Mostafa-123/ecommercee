            return $model->category->name;
        })
        ->addColumn(' ',function($model){
            return  '<img style="width: 25px; border-radius: 50%;" src="' . asset('images/products/' . $model->image) . '" alt="">';
        })
        ->rawColumns(['actions',' ','category_id'])
            ->setRowId('id');
    }

    /**
     * Get query source of dataTable.
     *
     * @param \App\Models\Product $model
     * @return \Illuminate\Database\Eloquent\Builder
     */
    public function query(Product $model): QueryBuilder
    {
        return $model->newQuery();
    }

    /**
     * Optional method if you want to use html builder.
     *
     * @return \Yajra\DataTables\Html\Builder
     */
    public function html(): HtmlBuilder
    {
        return $this->builder()
                    ->setTableId('products')
                    ->columns($this->getColumns())
                    ->minifiedAjax()
                    //->dom('Bfrtip')
                    ->orderBy([0, 'asc'])
                    ->selectStyleSingle();
    }

    /**
     * Get the dataTable columns definition.
     *
     * @return array
     */
    public function getColumns(): array
    {
        return [
            Column::make(' '),
            Column::make('id'),
            Column::make('title')->title('Name'),
            Column::make('description')->title('Description'),
            Column::make('quantity')->title('Quantity'),
            Column::make('price')->title('Price'),
            Column::make('discount_price')->title('Discount Price'),
            Column::make('category_id')->title('Category'),
            Column::make('created_at'),
            Column::make('actions'),
        ];
    }

    /**
     * Get filename for export.
     *
     * @return string
     */
    protected function filename(): string
    {
        return 'Products_' . date('YmdHis');
    }
}
